export const getAssets = () => ASSETS_ARRAY.slice();

export const getFiatAssets = () => ASSETS_ARRAY.filter(isAssetFiat);


export const isAssetTicker = <T extends string = string>(assetTicker: T): assetTicker is T & AssetTicker => {
  return assetTicker in ASSETS;
};

export const isAssetTickerFiat = <T extends string = string>(assetTicker: T): assetTicker is T & AssetTicker.Fiat => {
  return isAssetTicker(assetTicker) && ASSETS[assetTicker].type === ASSET_TYPE.Fiat;
};

export const isAssetTickerCrypto = <T extends string = string>(assetTicker: T): assetTicker is T & AssetTicker.Crypto => {
  return isAssetTicker(assetTicker) && ASSETS[assetTicker].type === ASSET_TYPE.Crypto;
};

export const isAsset = <T extends string = string>(asset: AssetConfigBase<T>): asset is AssetConfig<T & AssetTicker> => isAssetTicker(asset.symbol);
export const isAssetFiat = <T extends string = string>(asset: AssetConfigBase<T>): asset is AssetConfig<T & AssetTicker.Fiat> => isAssetTickerFiat(asset.symbol);
export const isAssetCrypto = <T extends string = string>(asset: AssetConfigBase<T>): asset is AssetConfig<T & AssetTicker.Crypto> => isAssetTickerCrypto(asset.symbol);


export const fromAssetTicker = <T extends AssetTicker>(assetTicker: T): AssetConfig<T> => {
  if (isAssetTicker(assetTicker)) return ASSETS[assetTicker] as AssetConfig<T>;

  throw new Error(`Unknown asset ticker passed to "fromAssetTicker": "${assetTicker}".`);
};

export const isFiatNetworkType = (networkType: ASSET_NETWORK): boolean => {
  const fiats = Object.values(ASSETS).filter(isAssetFiat);
  return fiats.some((asset) => asset.networks.some((network) => network.type === networkType));
};
