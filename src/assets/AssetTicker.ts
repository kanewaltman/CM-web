export interface AssetConfig<T extends AssetTicker = AssetTicker> extends AssetConfigBase<T> {
  /**
   * Type of the asset.
   */
  readonly type: T extends AssetTicker.Fiat ? ASSET_TYPE.Fiat
    : T extends AssetTicker.Crypto ? ASSET_TYPE.Crypto
      : never;
  /**
   * Networks where this asset can be deposited from and/or withdraw to.
   */
  readonly networks: ReadonlyArray<AssetNetwork>;
  /**
   * URL to icon, auto-generated by webpack. Ready to be used in `src` and `href` fields.
   */
  readonly icon: string;
  /**
   * Decimal places after the decimal point, where for fiats that's only the *official* one.
   * E.g. EUR has 2, although some other exchanges may trade it with 4 decimal places.
   */
  readonly decimalPlaces: number;
  /**
   * TODO: What is this?
   */
  readonly multiplier: number;
  /**
   * Is usable for collateral on margin platform.
   */
  readonly collateral?: boolean;
  /**
   * Is usable for tram.
   */
  readonly tram?: boolean;
  /**
   * In case `fromEveryAssetTickerFallback` returns a placeholder, e.g. for non-existing test assets.
   */
  readonly placeholder?: boolean;
  /**
   * Theme colors for light and dark mode
   */
  readonly theme: { light: string; dark: string };
}

